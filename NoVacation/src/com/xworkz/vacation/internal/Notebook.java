package com.xworkz.vacation.internal;

class Notebook {
    Notebook() {
        System.out.println("Notebook class");
    }

    public void open() {
        System.out.println("Opening the notebook.");
    }

    public void close() {
        System.out.println("Closing the notebook.");
    }

    public void write() {
        System.out.println("Writing notes in the notebook.");
    }

    public void flipPages() {
        System.out.println("Flipping through the pages.");
    }

    public void markImportant() {
        System.out.println("Marking important sections in the notebook.");
    }
}