package com.xworkz.fest.work;


class Lift {
    Lift() {
        System.out.println("Lift class");
    }

    public void moveUp() {
        System.out.println("Lift moves up.");
    }

    public void moveDown() {
        System.out.println("Lift moves down.");
    }

    public void stop() {
        System.out.println("Lift stops at a floor.");
    }

    public void carryPeople() {
        System.out.println("Lift carries people to different floors.");
    }

    public void indicateDirection() {
        System.out.println("Lift shows the direction of movement.");
    }

}




