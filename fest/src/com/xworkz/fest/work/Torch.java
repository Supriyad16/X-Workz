package com.xworkz.fest.work;


class Torch {
    Torch() {
        System.out.println("Torch class");
    }

    public void turnOn() {
        System.out.println("Torch is turned on.");
    }

    public void turnOff() {
        System.out.println("Torch is turned off.");
    }

    public void adjustBrightness() {
        System.out.println("Torch brightness can be adjusted.");
    }

    public void portable() {
        System.out.println("Torch is a portable light source.");
    }

    public void batteryOperated() {
        System.out.println("Torch usually runs on batteries.");
    }

}

