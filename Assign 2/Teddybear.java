class Teddybear{
	public static void dog(String surname){
		System.out.println("Printing the Teddybear class");
		System.out.println("Name:" + surname);
	}
	
	public static void elephant(int age){
		System.out.println("Age:" + age);	
	}
	
	public static void penguin(char group ){
		System.out.println("Group:" + group);	
	}
	
	public static void cat(double weight, double height ){
		System.out.println("Weight:" + weight);
		System.out.println("Height:" + height);		
	}
	
	public static void doll(int distance, String shape ){
		System.out.println("Distance:" + distance);
		System.out.println("Shape:" + shape);		
	}
	
	public static void rabbit(float time , double seconds, int item ){
		System.out.println("Time:" + time);
		System.out.println("Seconds:" + seconds);
		System.out.println("Item:" + item);
	}
	
	public static void lion(double milisec, String colour, int cost ){
		System.out.println("Milisec:" + milisec);
		System.out.println("Colour:" + colour);
		System.out.println("Cost:" + cost);
		System.out.println(" ");
	}
}