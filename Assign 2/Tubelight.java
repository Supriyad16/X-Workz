class Tubelight{
	public static void led(String type){
		System.out.println("Printing the Tubelight class");
		System.out.println("Name:" + type);
	}
	
	public static void zero(int age){
		System.out.println("Age:" + age);	
	}
	
	public static void candle(char group ){
		System.out.println("Group:" + group);	
	}
	
	public static void bright(double weight, double height ){
		System.out.println("Weight:" + weight);
		System.out.println("Height:" + height);		
	}
	
	public static void dim(int distance, String shape ){
		System.out.println("Distance:" + distance);
		System.out.println("Shape:" + shape);		
	}
	
	public static void bedlight(float time , double seconds, int item ){
		System.out.println("Time:" + time);
		System.out.println("Seconds:" + seconds);
		System.out.println("Item:" + item);
	}
	
	public static void lamp(double milisec, String colour, int cost ){
		System.out.println("Milisec:" + milisec);
		System.out.println("Colour:" + colour);
		System.out.println("Cost:" + cost);
		System.out.println(" ");
	}
}