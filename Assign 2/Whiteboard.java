class Whiteboard{
	public static void pen(String topic){
		System.out.println("Printing the Whiteboard class");
		System.out.println("Name:" + topic);
	}
	
	public static void duster(int age){
		System.out.println("Age:" + age);	
	}
	
	public static void marker(char group ){
		System.out.println("Group:" + group);	
	}
	
	public static void chalk(double weight, double height ){
		System.out.println("Weight:" + weight);
		System.out.println("Height:" + height);		
	}
	
	public static void eraser(int distance, String shape ){
		System.out.println("Distance:" + distance);
		System.out.println("Shape:" + shape);		
	}
	
	public static void pins(float time , double seconds, int item ){
		System.out.println("Time:" + time);
		System.out.println("Seconds:" + seconds);
		System.out.println("Item:" + item);
	}
	
	public static void sheet(double milisec, String colour, int cost ){
		System.out.println("Milisec:" + milisec);
		System.out.println("Colour:" + colour);
		System.out.println("Cost:" + cost);
		System.out.println(" ");
	}
}